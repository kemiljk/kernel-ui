/**
 * The single place that reads or writes the site's theme. Anything
 * that lets a visitor change accent, radius, or colour scheme —
 * ThemePlayground's "try it here" demo, the header's ThemeMenu, the
 * sun/moon ThemeToggle — calls through here instead of touching
 * `<html>` directly, so a change made in any one of them is visible in
 * all the others immediately and survives navigation.
 *
 * The storage keys here are duplicated (by necessity, not by
 * accident) in the inline no-FOUC script in BaseLayout.astro's
 * `<head>`, which has to stay a synchronous, dependency-free snippet
 * to run before first paint — it can't `import` this module. Keep the
 * three key strings in sync if either one changes.
 */

/** Every gamut-checked preset generated by
 * packages/styles/scripts/generate-accent-themes.mjs. Verified against
 * OKLCH-converted reference swatches there, not eyeballed, so e.g.
 * "violet" is actually violet. */
export const ACCENTS = ["amber", "blue", "green", "red", "violet", "orange", "teal"] as const;
export type AccentName = (typeof ACCENTS)[number];

export interface RadiusOption {
  name: string;
  value: string;
}

export const RADII: RadiusOption[] = [
  { name: "Sharp", value: "0.1875rem" },
  { name: "Soft", value: "0.5rem" },
  { name: "Round", value: "1.25rem" },
];

export type ColorScheme = "light" | "dark";

const STORAGE_KEYS = {
  accent: "kernel:accent",
  radius: "kernel:radius",
  scheme: "kernel:color-scheme",
} as const;

/**
 * ThemePlayground, ThemeMenu, and ThemeToggle are three separate Astro
 * islands (three separate React roots) that can independently change
 * the theme — they can't share React state directly, and
 * `window.addEventListener("storage", ...)` only fires in *other*
 * tabs/windows, never the one that made the change. This event is the
 * same-page equivalent: every `apply*` call below dispatches it, and
 * anything that renders a theme control listens for it to stay in
 * sync with changes made anywhere else on the same page.
 */
export const THEME_CHANGE_EVENT = "kernel:theme-change";

function announce() {
  window.dispatchEvent(new Event(THEME_CHANGE_EVENT));
}

export function applyAccent(name: AccentName) {
  document.documentElement.setAttribute("data-kernel-accent", name);
  localStorage.setItem(STORAGE_KEYS.accent, name);
  announce();
}

export function applyRadius(value: string) {
  document.documentElement.style.setProperty("--kernel-radius-base", value);
  localStorage.setItem(STORAGE_KEYS.radius, value);
  announce();
}

export function applyColorScheme(scheme: ColorScheme) {
  document.documentElement.setAttribute("data-theme", scheme);
  localStorage.setItem(STORAGE_KEYS.scheme, scheme);
  announce();
}

export function getStoredAccent(): AccentName | null {
  const value = localStorage.getItem(STORAGE_KEYS.accent);
  return value && (ACCENTS as readonly string[]).includes(value) ? (value as AccentName) : null;
}

export function getStoredRadiusValue(): string | null {
  return localStorage.getItem(STORAGE_KEYS.radius);
}

/** The radius option name (Sharp/Soft/Round) matching whatever value is
 * currently stored, if any — lets a UI highlight the active choice
 * without also hand-storing the name alongside the value. */
export function getStoredRadiusName(): string | null {
  const value = getStoredRadiusValue();
  return RADII.find((radius) => radius.value === value)?.name ?? null;
}

export function getStoredColorScheme(): ColorScheme | null {
  const value = localStorage.getItem(STORAGE_KEYS.scheme);
  return value === "light" || value === "dark" ? value : null;
}
